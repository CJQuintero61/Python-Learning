# use len(variableNameHere) to get the length
greeting = "Welcome to Python!"

print("Greeting has " + str(len(greeting)) + " characters in it")

if "Welcome" in greeting:
    print("'Welcome' is in the greeting variable")

if "Java" not in greeting:
    print("'Java' is not in the greeting")