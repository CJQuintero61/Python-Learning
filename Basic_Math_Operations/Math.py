# subtraction
print(5 - 16)

# multiplication
print(3 * -4)

# division
# regular division always returns a float
print(4 / 16)

# floored division
print(4 // 3)

# modulus 
print(20 % 3)

# power
print(2 ** 7)

# mixed data types
# scientific, float, int
print(3.591736492e-4 * 2.98 + -6)